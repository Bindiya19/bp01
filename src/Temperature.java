public class Temperature {
  static void CtoF(double c){
     double f = (c = 9/5 * 0 ) + 32;
     System.out.println("the temperature is in fahrenheit for given celsius " + c + " is: " + f );
 }
 public static void main(String[]args){
     Temperature temperature=new Temperature();
     temperature.CtoF(55);
 }
}
